package in.sp.beans;

public class Student {
	private int std_roll;
	private String std_name;
	private int std_marks;

	public int getStd_roll() {
		return std_roll;
	}

	public void setStd_roll(int std_roll) {
		this.std_roll = std_roll;
	}

	public String getStd_name() {
		return std_name;
	}

	public void setStd_name(String std_name) {
		this.std_name = std_name;
	}

	public int getStd_marks() {
		return std_marks;
	}

	public void setStd_marks(int std_marks) {
		this.std_marks = std_marks;
	}

}
